public class Array04 {
    public static void main(String[] args) {
        char[] n= new char[7];

        n[0]='c';
        n[1]='u';
        n[2]='a';
        n[3]='d';
        n[4]='r';
        n[5]='o';
        n[6]='s';
      

        System.out.println("El array n imprime los siguientes elementos: ");

        for (int i = 0; i < 7; i++) {
            System.out.print(n[i]);
        }


    }
}
